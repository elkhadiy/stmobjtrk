int CAMERA_Initialization_sequence() {

	HAL_GPIO_WritePin(DCMI_PWR_EN_GPIO_Port, DCMI_PWR_EN_Pin, GPIO_PIN_RESET);
	HAL_Delay(3);

	if (HAL_I2C_IsDeviceReady(&hi2c1, 0x60, 10, 1000) == HAL_OK) {

		printf("Camera bus ready!\r\n");

		uint16_t verpid = 0;
		HAL_I2C_Mem_Read(&hi2c1, 0x60, 0x0A, I2C_MEMADD_SIZE_8BIT, (uint8_t*)&verpid, 2, 1000);
		printf("PIR : %04x\r\n", verpid);

		if (verpid == 0x5796) {

			uint8_t sccb_reg_reset = 0x80;
			HAL_I2C_Mem_Write(&hi2c1, 0x60, 0x12, I2C_MEMADD_SIZE_8BIT, &sccb_reg_reset, 1, 1000);
			HAL_Delay(200);

			for (int i = 0; i < sizeof(OV9655_QVGA) / 2; i++) {

				HAL_I2C_Mem_Write(&hi2c1, 0x61, OV9655_QVGA[i][0], I2C_MEMADD_SIZE_8BIT, (uint8_t *)&OV9655_QVGA[i][1], 1, 1000);
				HAL_Delay(2);
				uint8_t rdbk;
				HAL_I2C_Mem_Read(&hi2c1, 0x60, OV9655_QVGA[i][0], I2C_MEMADD_SIZE_8BIT, (uint8_t *)&rdbk, 1, 1000);

				if (rdbk != OV9655_QVGA[i][1])
					printf("Failed read back of reg %02x: %02x [%02x]\r\n", OV9655_QVGA[i][0], OV9655_QVGA[i][1], rdbk);
			}

		} else {
			printf("Wrong id %04x should be 5796\r\n", verpid);
			return -1;
		}

		return 1;

	} else {

		printf("Can't find camera\r\n");
		return -1;
	}
}


void CAMERA_Start_Crop_Capture_To_Video_Buffer() {

	HAL_DCMI_ConfigCrop(&hdcmi, (640 - 480) / 2, (480 - 272) / 2, (480 * 2) - 1, 272 - 1);

	HAL_DCMI_EnableCrop(&hdcmi);

	HAL_DCMI_Start_DMA(&hdcmi, DCMI_MODE_CONTINUOUS, 0xC0000000, 2 * 480 * 272);

}


void CAMERA_Start_Capture() {

	HAL_DCMI_Start_DMA(&hdcmi, DCMI_MODE_CONTINUOUS, 0xC0000000 + 2 * 480 * 272, 2 * 320 * 240);

}


